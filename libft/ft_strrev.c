char	*ft_strrev(char *str)
{
	char	c;
	int		x;
	int		y;

	x = 0;
	while (str[x] != '\0')
		x++;
	x = x - 1;
	if (x < 1)
		return (str);
	y = 0;
	while (x > y)
	{
		c = str[x];
		str[x] = str[y];
		str[y] = c;
		x--;
		y++;
	}
	return (str);
}
