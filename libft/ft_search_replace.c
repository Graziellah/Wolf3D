#include "libft.h"

char	*ft_search_replace(char *s, char a, char b)
{
	size_t		i;

	i = 0;
	while (s[i])
	{
		if (s[i] == a)
			s[i] = b;
		i++;
	}
	s[i] = '\0';
	return (s);
}
